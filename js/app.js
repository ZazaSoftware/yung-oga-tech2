
// Set the date we're counting down to (change this to your desired date)
let saleEndDate = new Date("Jan 31, 2025 23:59:59").getTime();

// Update the countdown every 1 second
let countdownInterval = setInterval(function() {
    // Get the current date and time
    let now = new Date().getTime();

    // Find the distance between now and the sale end date
    let distance = saleEndDate - now;

    // Calculate days, hours, minutes, and seconds
    let days = Math.floor(distance / (1000 * 60 * 60 * 24));
    let hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
    let minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
    let seconds = Math.floor((distance % (1000 * 60)) / 1000);

    // Display the result in the countdown paragraph
    document.getElementById("countdown").innerHTML = "Hurry up! sale ends in:" +
        days + "d " + hours + "h " + minutes + "m " + seconds + "s ";

    // If the countdown is over, display a message
    if (distance < 0) {
        clearInterval(countdownInterval);
        document.getElementById("countdown").innerHTML = "Sale Ended";
    }
}, 1000);



